package cern.devtools.deps.domain;

import java.util.List;

/**
 * Abstract interface of all elements which is stored in the database.
 * 
 * @author Donat Csikos
 */
public interface CodeElement {

	/**
	 * Returns the id of the objects which should be generated by the database.
	 * 
	 * @return The id of the entity or 0, it the object is transient (not stored);
	 */
	public long getId();

	/**
	 * Sets the id of the objects which should be generated by the database.
	 * 
	 * @param id The id of the objects which should be generated by the database.
	 */
	public void setId(long id);

	/**
	 * Every code element should specify which product versions are they exist in it.
	 * 
	 * @return The list of product versions which the product is contained in.
	 */
	public List<String> getVersions();
	
	/**
	 * Add new version to the versions list.
	 * 
	 * @param version the new version number.
	 * @return
	 */
	public void addVersions(String... versions);

	/**
	 * Returns a human-readable possibly short representation, which could be used on visualisation.
	 * 
	 * @return human-readable string.
	 */
	public String getDisplayName();
}
